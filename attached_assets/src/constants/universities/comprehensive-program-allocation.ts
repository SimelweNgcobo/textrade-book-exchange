// Helper function to generate university-specific APS scores
const generateUniversityAPS = (
  baseAPS: number,
  universityType: string,
  universityId: string,
  isCompetitive: boolean = false,
): number => {
  let apsScore = baseAPS;

  // Adjust based on university prestige and type
  const prestigeAdjustments: Record<string, number> = {
    uct: 3,
    wits: 3,
    su: 2,
    up: 2,
    ukzn: 1,
    ufs: 1,
    tut: -1,
    dut: -1,
    mut: -2,
    vut: -2,
    cput: -1,
    cut: -2,
    uj: 0,
    unisa: -3,
    unizul: -1,
    uv: -1,
    wsu: -2,
    ump: -1,
  };

  apsScore += prestigeAdjustments[universityId] || 0;

  // Additional adjustment for competitive programs
  if (isCompetitive) {
    apsScore += universityId === "uct" || universityId === "wits" ? 2 : 1;
  }

  // Ensure minimum APS of 20 and maximum of 42
  return Math.max(20, Math.min(42, apsScore));
};

